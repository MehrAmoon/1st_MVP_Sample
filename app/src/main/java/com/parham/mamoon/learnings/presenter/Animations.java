public class Animations {
    int duration, delay;
    float firstAlpha, secondAlpha;

    public Animations() {
    }

    public int getDuration() {
        return duration;
    }

    public void setDuration(int duration) {
        this.duration = duration;
    }

    public float getFirstAlpha() {
        return firstAlpha;
    }

    public void setFirstAlpha(float firstAlpha) {
        this.firstAlpha = firstAlpha;
    }

    public float getSecondAlpha() {
        return secondAlpha;
    }

    public void setSecondAlpha(float secondAlpha) {
        this.secondAlpha = secondAlpha;
    }

    public int getDelay() {
        return delay;
    }

    public void setDelay(int delay) {
        this.delay = delay;
    }
}
