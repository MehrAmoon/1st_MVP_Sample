public class ImageViewParams {

    int left_margin,top_margin,right_margin,bottom_margin ,setBackgroundResource,height, width;
    String setMargin , gravity , setOrientation ,setText ,LayoutParams,setVisibility ;

    public ImageViewParams() {
    }

    public int getSetBackgroundResource() {
        return setBackgroundResource;
    }

    public void setSetBackgroundResource(int setBackgroundResource) {
        this.setBackgroundResource = setBackgroundResource;
    }

    public String getSetMargin() {
        return setMargin;
    }

    public void setSetMargin(String setMargin) {
        this.setMargin = setMargin;
    }

    public int getLeft_margin() {
        return left_margin;
    }

    public void setLeft_margin(int left_margin) {
        this.left_margin = left_margin;
    }

    public int getTop_margin() {
        return top_margin;
    }

    public void setTop_margin(int top_margin) {
        this.top_margin = top_margin;
    }

    public int getRight_margin() {
        return right_margin;
    }

    public void setRight_margin(int right_margin) {
        this.right_margin = right_margin;
    }

    public int getBottom_margin() {
        return bottom_margin;
    }

    public void setBottom_margin(int bottom_margin) {
        this.bottom_margin = bottom_margin;
    }

    public String getGravity() {
        return gravity;
    }

    public void setGravity(String gravity) {
        this.gravity = gravity;
    }

    public String getSetOrientation() {
        return setOrientation;
    }

    public void setSetOrientation(String setOrientation) {
        this.setOrientation = setOrientation;
    }

    public String getSetText() {
        return setText;
    }

    public void setSetText(String setText) {
        this.setText = setText;
    }

    public String getLayoutParams() {
        return LayoutParams;
    }

    public void setLayoutParams(String layoutParams) {
        LayoutParams = layoutParams;
    }

    public String getSetVisibility() {
        return setVisibility;
    }

    public void setSetVisibility(String setVisibility) {
        this.setVisibility = setVisibility;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
